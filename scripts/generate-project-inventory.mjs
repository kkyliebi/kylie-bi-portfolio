import { mkdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import process from 'node:process';

const root = process.cwd();
const contentRoot = path.join(root, 'src/content');
const indexSource = await readFile(path.join(contentRoot, 'index.ts'), 'utf8');
const imports = new Map(
  [...indexSource.matchAll(/import \{ (\w+) \} from '\.\/(.+)';/g)].map((match) => [match[1], match[2]]),
);
const configuredProjects = indexSource
  .match(/export const projects = \[([\s\S]*?)\] satisfies/)?.[1]
  .split(',')
  .map((item) => item.trim())
  .filter(Boolean) ?? [];

const rows = [];

for (const projectName of configuredProjects) {
  const folder = imports.get(projectName);

  if (!folder) {
    throw new Error(`No content folder import found for ${projectName}.`);
  }

  const metadata = await readFile(path.join(contentRoot, folder, 'metadata.ts'), 'utf8');
  const narrative = await readFile(path.join(contentRoot, folder, 'narrative.ts'), 'utf8');
  const value = (field) => metadata.match(new RegExp(`^\\s*${field}:\\s*(['\"])(.+)\\1,$`, 'm'))?.[2] ?? 'Missing';
  const status = value('contentStatus');

  rows.push({
    title: value('title'),
    route: value('path'),
    category: value('category'),
    narrative: status === 'draft' ? 'Draft' : status === 'verified' ? 'Verified' : 'Unclassified',
    assets: 'Missing',
  });
}

const output = `# Generated project inventory

> Generated by \`npm run docs:inventory\`. Do not edit by hand.

| Project | Route | Category | Narrative | Production assets |
| --- | --- | --- | --- | --- |
${rows.map((row) => `| ${row.title} | \`${row.route}\` | ${row.category} | ${row.narrative} | ${row.assets} |`).join('\n')}

## Coverage

- Projects configured: ${rows.length}
- Draft narratives: ${rows.filter((row) => row.narrative === 'Draft').length}
- Verified narratives: ${rows.filter((row) => row.narrative === 'Verified').length}
- Projects with production assets: 0
`;

await mkdir(path.join(root, 'docs/generated'), { recursive: true });
await writeFile(path.join(root, 'docs/generated/project-inventory.md'), output);
